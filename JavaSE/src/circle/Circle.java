package circle;

public class Circle {
    public static void main(String[] args) {
        int num1 = 0;
        int num2 = 0;
        while(num1<0){
            System.out.println("num1:"+num1);
            num1++;
        }
        do {
            System.out.println("num2:"+num2);
            num2++;
        }while(num2<0);
    }
}
